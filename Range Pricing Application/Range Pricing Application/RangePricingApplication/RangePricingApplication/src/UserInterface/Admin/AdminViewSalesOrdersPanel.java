    private void loadTableData(final String salName, final String custName,
            final String supName, final String prodName) {
        if (business != null) {
            if (business.getMasterOrderList() != null
                    && !business.getMasterOrderList().getOrders().isEmpty()) {
                int rowIndex = 1;
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                for (Order order : business.getMasterOrderList().getOrders()) {
                    Object[] row = new Object[6];
                    row[0] = rowIndex;
                    row[1] = order.getCustomer().getCustomerId();
                    row[2] = order.getSalesperson().getPerson().getPersonId();
                    row[3] = order.getOrderTotal();
                    row[4] = order.getCountOrderItems();
                    row[5] = "";
                    dtm.addRow(row);
                    rowIndex++;
                }
            }
        }
    }

