    private void loadTableData() {
        if (business != null) {
            MarketCatalog mc = business.getMarketCatalog();
            int rowIndex = 1;
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for (Market ma : mc.getMarkets()) {
                Object[] row = new Object[10];
                row[0] = rowIndex;
                row[1] = ma.getName();
                dtm.addRow(row);
                rowIndex++;
            }
        }
    }

