    private void loadMarkets() {
        DefaultComboBoxModel makcb = (DefaultComboBoxModel) marketCB.getModel();
        makcb.removeAllElements();
        makcb.addElement("Select");
        Set<String> prodSet = new HashSet<>();
        MarketCatalog mc = business.getMarketCatalog();
        for (Market ma : mc.getMarkets()) {
            prodSet.add(ma.getName());
        }
        if (!prodSet.isEmpty()) {
            makcb.addAll(prodSet);
        }
    }

    private void loadTableData() {
        if (business != null) {
            MarketCatalog mc = business.getMarketCatalog();
            int rowIndex = 1;
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for (Market ma : mc.getMarkets()) {
                for (Channel ch : ma.getValidchannels()) {
                    Object[] row = new Object[3];
                    row[0] = rowIndex;
                    row[1] = ma.getName();
                    row[2] = ch.getChannelType();
                    dtm.addRow(row);
                    rowIndex++;
                }
            }
        }
    }

